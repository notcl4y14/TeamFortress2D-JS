let Position2D = function (x, y) {
	this.x = x;
	this.y = y;
};

let Position3D = function (x, y, z) {
	this.x = x;
	this.y = y;
	this.z = z;
};

let Dim2D = function (w, h) {
	this.width = w;
	this.height = h;
};

let Dim3D = function (w, h, l) {
	this.width = w;
	this.height = h;
	this.length = l;
};